// CSSColor returns the color for the given CSS color name.
// See https://developer.mozilla.org/en-US/docs/Web/CSS/color_value.
func CSSColor(name string) (color.Color, bool) {
	color, ok := cssColorCodes[strings.ToLower(name)]
	return color, ok
}
